def main():
    # Load en.json to get all keys
    with open('backend/i18n/en.json', 'r', encoding='utf-8') as f:
        en_data = json.load(f)
    
    # Load vi.json
    with open('backend/i18n/vi.json', 'r', encoding='utf-8') as f:
        vi_data = json.load(f)
    
    # Update vi.json with translations
    for key in en_data:
        if key == '_meta':
            continue
        if key in TRANSLATIONS:
            vi_data[key] = TRANSLATIONS[key]
        else:
            # Keep empty if no translation available
            if key not in vi_data:
                vi_data[key] = ""
    
    # Update meta
    vi_data['_meta']['source'] = "Step 2: AI-translated UI strings from en.json"
    
    # Save vi.json
    with open('backend/i18n/vi.json', 'w', encoding='utf-8') as f:
        json.dump(vi_data, f, ensure_ascii=False, indent=2)
    
    print("Translated vi.json with UI strings")
    print(f"Translated {len([k for k in vi_data if k != '_meta' and vi_data[k]])} keys")

if __name__ == '__main__':
    main()
